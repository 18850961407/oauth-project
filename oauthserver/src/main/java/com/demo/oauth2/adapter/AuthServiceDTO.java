package com.demo.oauth2.adapter;

public class AuthServiceDTO {

	private String authServiceId;
	
	private String authServiceName;
	
	private String authType;

	public String getAuthServiceId() {
		return authServiceId;
	}

	public void setAuthServiceId(String authServiceId) {
		this.authServiceId = authServiceId;
	}

	public String getAuthServiceName() {
		return authServiceName;
	}

	public void setAuthServiceName(String authServiceName) {
		this.authServiceName = authServiceName;
	}

	public String getAuthType() {
		return authType;
	}

	public void setAuthType(String authType) {
		this.authType = authType;
	}
	
	
}
